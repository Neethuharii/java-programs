package com.bmc.basicprojects;

public class Pattern5 {

	public static void main(String[] args) {
		int count=2;
		for(int i=1;i<=3;i++) {
			count=count+2;
			for(int j=1;j<=2;j++) {
				
				System.out.print( count + " ");
				count++;
			}
			System.out.println();
	}

	

	}

}
